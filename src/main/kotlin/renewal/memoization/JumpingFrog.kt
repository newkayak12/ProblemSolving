    private fun fibonacci(memoization: MutableMap<Int, Int>, number: Int): Int  {
        return when (number) {
            0 -> 0
            1 -> 1
            else -> memoization[number] ?: run {
                val result = fibonacci(memoization, number - 2) + fibonacci(memoization, number - 1)
                memoization[number] = result
